async function loadBase64Image(base64Str) {
    if (!/^data:image\/(png|jpeg|jpg);base64,/.test(base64Str)) {
        throw new Error("Unsupported image format");
    }

    const base64Data = base64Str.replace(/^data:image\/\w+;base64,/, "");
    const buffer = Buffer.from(base64Data, "base64");
    return loadImage(buffer);
}


app.post('/api/generate-image', async (req, res) => {
    const { mockupId, wallpaperImage, type, patch_size } = req.body;

    const env = envs.find(item => item.mockup_id == mockupId);

    if (!env) {
        return res.status(400).send('Invalid mockup selection');
    }

    const mainImageSrc = env.space1;
    const { leftCord, topCord, width, height, mockup_size } = env;

    try {
        const mainImage = await loadAnyImage(mainImageSrc);
        const wallpaper = await loadBase64Image(wallpaperImage);

        const mainCanvas = createCanvas(mainImage.width, mainImage.height);
        const ctx = mainCanvas.getContext("2d");

        let blended;

        if (type === "concept") {
            blended = conceptCanvas(wallpaper, width, height);
        } else {
            blended = patternCanvas(wallpaper, mockup_size, patch_size, width, height);
        }

        /* WALL AREA */
        ctx.drawImage(
            blended,
            leftCord,
            topCord,
            width,
            height
        );

        /* MOCKUP OVERLAY */
        ctx.drawImage(mainImage, 0, 0);  // Use mainImage instead of mockup

        // Send base64 PNG image
        res.json(mainCanvas.toDataURL("image/png"));

    } catch (error) {
        console.error('Error while processing images:', error);
        res.status(500).send('Error generating image');
    }
});

// Pattern Draw Logic (Repeat Wallpaper as a Tile with Patch Size)
function patternCanvas(wallpaper, mockupSize, patchSize, width, height) {
    // Parse mockup size (e.g., "10x12" -> { width: 10, height: 12 })
    const { width: mwidth, height: mheight } = getPatchSize(mockupSize);

    // 1. REAL WALL SIZE IN INCHES
    const realWallW = mheight * 12;  // Convert height from inches to pixels (1 inch = 12 pixels)
    const realWallH = mwidth * 12;

    // 2. CANVAS SCALE (Adjust the canvas size relative to the real wall size)
    const canvasWpx = 800;  // Example canvas width (you can set this based on your requirements)
    const pxPerIn = canvasWpx / realWallW;  // Pixels per inch
    const canvasHpx = Math.round(realWallH * pxPerIn);

    // 3. PATCH SIZE CONFIGURATION
    const patch = getPatchSize(patchSize);  // Convert patch size to width/height in inches

    // Convert patch size to pixels
    const tileWpx = Math.round(patch.width * pxPerIn);  // Width in pixels
    const tileHpx = Math.round(patch.height * pxPerIn);  // Height in pixels

    // 4. OUTPUT CANVAS
    const outputCanvas = createCanvas(canvasWpx, canvasHpx);
    const ctx = outputCanvas.getContext("2d");

    // Clear the output canvas before filling with the pattern
    ctx.clearRect(0, 0, canvasWpx, canvasHpx);

    // 5. TILE CANVAS (Create a tile pattern canvas)
    const tileCanvas = createCanvas(tileWpx, tileHpx);
    const tileCtx = tileCanvas.getContext("2d");

    // Draw wallpaper on the tile canvas
    tileCtx.drawImage(wallpaper, 0, 0, tileWpx, tileHpx);

    // 6. FILL PATTERN (Repeat the tile pattern on the output canvas)
    const pattern = ctx.createPattern(tileCanvas, "repeat");
    ctx.fillStyle = pattern;
    ctx.fillRect(0, 0, canvasWpx, canvasHpx);

    return outputCanvas;
}

function conceptCanvas(wallpaper, width, height) {
    const canvas = createCanvas(width, height);
    const ctx = canvas.getContext("2d");

    ctx.clearRect(0, 0, width, height);
    ctx.globalCompositeOperation = "multiply";
    ctx.drawImage(wallpaper, 0, 0, width, height);
    ctx.globalCompositeOperation = "source-over";

    return canvas;
}

// Helper function to parse patch size
function getPatchSize(patchSize) {
    const [w, h] = patchSize
        .toLowerCase()
        .replace("Ã—", "x")
        .split("x")
        .map(Number);
    return { width: w, height: h };
}

// Image loader function
async function loadAnyImage(src) {
    if (src.startsWith("http")) {
        const res = await fetch(src);
        const buf = await res.buffer();
        return loadImage(buf);
    }
    return loadImage(src);
}

